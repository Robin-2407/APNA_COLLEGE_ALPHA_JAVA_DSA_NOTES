public class Basics {
    public static void main(String[] args) {
        // Binary And
        System.out.println(5 & 1);

        // Binary Or
        System.out.println(5 | 1);

        // Binary XOR
        System.out.println(~1);

        // Binary Left Shift
        System.out.println(5 << 2);

        // Binary Right Shift
        System.out.println(5 >> 1);

    }
}
